
import math

print(math.sqrt(16))
print(math.ceil(10.098))        # returns the ceiling of x as  a integer rounds to the highest number in this case.
print(math.copysign(10,+1))     # returns x with the sign of y.it supports (1.0,-0.0)  returns -1.0)
print(math.fabs(-19.7))         # returns positive interger of entered negative value.
print('---')
# power and logrithmic functions

print(math.exp(2))
print(math.expm1(2))
print(math.log(10,10))          #   with one argument return the natural algorithm of x(to base 10  )
print('----')
# Trigonometric functions:

print(math.acos(.5))            # return the arc cosine of x, in radians
print(math.asin(.5))            # return the arc sign of x in radians
print(math.atan(5))             # return the arc tangent of x i radians
print(math.cos(3))              #   return the cosine of x radians
print('---')
# angular and hyperbolic functions:

print(math.degrees(0.1))        # convert angle x from radians to degrees
print(math.radians(5.7279))      #  convert angle x from degrees to radians
print(math.pi)              #   returns the value of constant pi
print(math.e)                 # returns the value of constant e


