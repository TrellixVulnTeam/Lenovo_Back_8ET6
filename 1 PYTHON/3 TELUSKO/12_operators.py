# Assignment Operators:
x = 2
x += 3
print(x)
x *= 5
print(x)
x /= 5
print(x)

a, b, c = 1, 5, 6.3
print(a, b)


# Unary Operator
n = 4
n = -n
print(n)

# Relational Operators
a = 20
b = 20.1
print(a == b)
print(a != b)


# Logical Operators
a, v = 3, 9
print(a < 5 and v < 10)
print(a < 5 or v < 10)
print(not a)
print(not v)
